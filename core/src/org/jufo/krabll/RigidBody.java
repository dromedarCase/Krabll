package org.jufo.krabll;


public class RigidBody {
    int xCoordinate;
    int yCoordinate;
    short rotation;
    
    public RigidBody(int xCoordinate, int yCoordinate, short rotation){
        this.xCoordinate = xCoordinate;
        this.yCoordinate = yCoordinate;
        this.rotation = rotation;
    }
}
